SELECT 
    CODIGO_PESSOA,
    RAZAOSOCIAL_PESSOA,
    GRUPO_PESSOA,
    DATAHORAINCLUSAO_PESSOA,
    COUNT(*) OVER() AS TOTAL_REGISTROS
FROM PESSOA  
WHERE DATAHORAINCLUSAO_PESSOA BETWEEN Filtro(1,0) AND Filtro(2,0) AND
    GRUPO_PESSOA = 0 AND
    STATUS_PESSOA = 0 AND
    TIPO_PESSOA = 'J'
ORDER BY DATAHORAINCLUSAO_PESSOA DESC;